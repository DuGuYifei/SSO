package lsea.utils;

/**
 * Utility enum class representing the possible log types in the SSO system.
 */
public enum LogType {

    /**
     * Success: a log entry representing a successful operation.
     */
    Success,

    /**
     * Fail: a log entry representing a failed operation.
     */
    Fail,

    /**
     * Forbidden: a log entry representing a forbidden operation.
     */
    Forbidden,

    /**
     * NotFound: a log entry representing a not found operation.
     */
    NotFound

}
